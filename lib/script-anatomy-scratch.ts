/**
 * Generic short-form script anatomy for "create from scratch" prompts.
 * Not tied to any single creator — safe to inject for all clients.
 */

export const SCRATCH_SCRIPT_ANATOMY_BLOCK = `
=== REFERENCE ANATOMY (SHORT-FORM REEL) ===
Aim for this **flow** unless the chosen story structure explicitly demands a variant:

1. **Hook** — Stop the scroll in the first 1–2 lines. One clear tension, question, or contrast. Do not give away the full payoff.
2. **Intro / bridge** — One tight line that frames who this is for and what they will get (can merge with hook on very short lengths).
3. **Problem** — Name the belief or behavior, validate it, then reveal why it hurts them *now*.
4. **Explanation** — Simple "why" (plain language; define any technical term in one short phrase).
5. **Steps** — 2–4 actionable beats; each doable today; no filler.
6. **Closing** — Hopeful reframe tied to the topic (not a guilt trip, not a fake question stack).
7. **CTA** — One ask only; make the **reason** to act come **before** the trigger word (comment / save / follow).

**Pacing:** Re-open curiosity every ~10–15s in the body (micro "but / here's the thing / most people miss" beats).

**Mute thinking:** Spoken lines should still make sense if half the viewers watch on mute (clear progression; avoid inside jokes that only work with sound).
=== END REFERENCE ANATOMY ===
`.trim();
